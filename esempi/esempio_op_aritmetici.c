#include <math.h>

int main() {
    // Esempi di utilizzo degli operatori aritmetici
    float a = 10 + 1;
    float b = 20 - 3;
    float c = 3 * 2;
    float d = 10 / 3; // Divisione intera
    float e = 10 / 3.0; // Divisione floating point
    float g = 10 % 3; // Resto della divisione intera
    float h = 10 / (float) 3; // Divisione floating point (con cast)
    return 0;
}